package b.switching;

public class SwitchDemo6String {

	public static void main(String[] args) {

		String str = "BBB";

		switch (str) {
		case "AAA":
			System.out.println(1);
			break;
		case "BBB":
			System.out.println(2);
			break;
		case "CCC":
			System.out.println(3);
			break;
		}

	}
}
