package e;

/**
 * this demo shows all Java primitives
 * 
 * @author Eldar
 */
public class BitsAndByts {

	public static void main(String[] args) {

		// numerals integers or decimals
		System.out.println(5); // integer
		System.out.println(5.32); // decimal

		// integer numerals
		byte x1 = 5; // 8 bit 2^8 = 256
		short x2 = 5; // 16 bit
		int x3 = 5; // 32 bit
		long x4 = 5; // 64 bit

		// bit - Binary DigiT
		// סיבית - ספרה בינארית

		// 0, 1, 10, 11, 100
		// 00000000
		// 00000001
		// 11111111
	}

}
