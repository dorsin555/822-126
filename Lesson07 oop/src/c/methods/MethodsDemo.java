package c.methods;

public class MethodsDemo {

	// methods can get parameters and return nothing - void
	void m1(int x) {
		System.out.println(x);
	}

	// methods can get parameters and return values
	int m2(int x) {
		return x;
	}

	// methods can get no parameters and return values
	int m3() {
		return 0;
	}

	// methods can get no parameters and return nothing - void
	void m4() {
	}
}
